# != ოპერატორის ტესტი
print(5 != 3)  # True, რადგან 5 არ უდრის 3-ს
print(5 != 5)  # False, რადგან 5 ტოლია 5-ის

# >= ოპერატორის ტესტი
print(5 >= 3)  # True, რადგან 5 მეტია 3-ზე
print(3 >= 5)  # False, რადგან 3 არ არის 5-ზე მეტი ან ტოლი

# <= ოპერატორის ტესტი
print(3 <= 5)  # True, რადგან 3 ნაკლებია ან ტოლია 5-ის
print(5 <= 3)  # False, რადგან 5 არ არის 3-ზე ნაკლები ან ტოლი
