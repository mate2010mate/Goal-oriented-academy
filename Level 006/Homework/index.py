# ცვლადების შექმნა
num1 = 15
num2 = 7
num3 = 3

# ძირითადი მათემატიკური ოპერაციები
addition = num1 + num2 + num3  # ჯამი
subtraction = num1 - num2 - num3  # გამოკლება
multiplication = num1 * num2 * num3  # გამრავლება
division = num1 / num2  # გაყოფა
integer_division = num1 // num3  # მთელ რიცხვებზე გაყოფა
modulus = num1 % num2  # ნაშთი გაყოფისას
power = num1 ** num3  # ხარისხში აყვანა

# შედეგების დაბეჭდვა
print("Addition:", addition)
print("Subtraction:", subtraction)
print("Multiplication:", multiplication)
print("Division:", division)
print("Integer Division:", integer_division)
print("Modulus:", modulus)
print("Power:", power)
