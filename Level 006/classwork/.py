# Best Practice: snake_case სახელების გამოყენება ცვლადებისთვის
first_dog_name = "Rick"
second_dog_name = "John"

# მნიშვნელობის გადაწერა second_dog_name-ისთვის
second_dog_name = "Max"

# Best Practice: კომენტარების დამატება და სწორი ფორმატირება
# ძაღლების სახელების გამოტანა format() ფუნქციის გამოყენებით უკეთესი წაკითხვისთვის
print(
    f"First dog's name is {first_dog_name}. Second dog's name is {second_dog_name}."
)

