# ლოგიკური და შედარების ოპერატორების კომბინაციები
print(5 > 3 and 8 == 8)    # True, რადგან ორივე პირობაა სწორი
print(10 < 20 or 5 != 5)   # True, რადგან პირველი პირობა არის სწორი
print(not(5 == 5))         # False, რადგან 5 == 5 მართალია, მაგრამ not შეცვლის შედეგს
print(7 <= 10 and 8 != 8)  # False, რადგან მეორე პირობა არასწორია
print(6 > 4 or 3 < 1)      # True, რადგან პირველი პირობა მართალია
