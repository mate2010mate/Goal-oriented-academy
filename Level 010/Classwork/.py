for number in range(50, 101):  # 50-დან 100-მდე (101 არ შედის)
    print(number)
for number in range(10, 0, -1):  # 10-დან 1-მდე, -1 ნაბიჯით (უკუსვლა)
    print(number)
for _ in range(100):  # 0-დან 99-მდე (100-ჯერ)
    print("Hello world")
for i in range(1, 101):  # 1-დან 100-მდე
    print(i)  # ნომერი
    print("Hello world")  # ტექსტი

