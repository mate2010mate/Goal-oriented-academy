age1 = float(input("შეიყვანეთ პირველი პირის ასაკი: "))

#მომხმარებლის მეორე ასაკი
age2 = float(input("შეიყვანეთ მეორე პირის ასაკი: "))

#მახსოვს, რომ მეორე ასაკი უფროსია
print("მეორე პირი უფროსია.") if age1 < age2 else None

#მახსოვს, რომ პირველი ასაკი უფროსია
print("პირველი პირი უფროსია.") if age1 > age2 else None

