# ცვლადის შექმნა
number = 5  # number თავდაპირველად არის 5
print("Initial value:", number)

# მნიშვნელობის გადაწერა
number = number + 10  # number-ის მნიშვნელობა ახლა ხდება 15
print("Reassigned value:", number)

# კომენტარი:
# აქ number = number + 10 ნიშნავს, რომ ვიღებთ ცვლადის ძველ მნიშვნელობას (5),
# ვუმატებთ 10-ს და ახალ მნიშვნელობას (15) კვლავ number-ს ვანიჭებთ.
# მნიშვნელობის გადაწერა საშუალებას გვაძლევს, ცვლადის მონაცემი შევცვალოთ კოდის მიმდინარეობისას.
